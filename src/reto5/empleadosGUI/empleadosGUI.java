    public void mostrarDatos3(){
         
        
        while(modelo.getRowCount()>0){
            modelo.removeRow(0);
        }
        conexion objetoConexion = new conexion();
        try{
            ResultSet resultado = objetoConexion.consultarRegistros("SELECT c.ID_Compra ,a.Constructora , a.Banco_Vinculado FROM Proyecto a join Compra c on a.ID_Proyecto = c.ID_Proyecto WHERE a.Clasificacion = 'Casa Campestre' AND  c.Proveedor = 'Homecenter'");
            while(resultado.next()){
                resultado.getString("ID_Compra");
                resultado.getString("Constructora");
                resultado.getString("Banco_Vinculado");
                
                Object[] oUser = {resultado.getString("ID_Compra"),resultado.getString("Constructora"),resultado.getString("Banco_Vinculado")};
                modelo.addRow(oUser);
            }
        }
        catch(Exception e){
            System.out.println(e);
        }
        
    }
    
