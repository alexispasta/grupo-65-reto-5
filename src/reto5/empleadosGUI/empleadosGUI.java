    public void mostrarDatos(){
        
        
        while(modelo.getRowCount()>0){
            modelo.removeRow(0);
            
        }
        conexion objetoConexion = new conexion();
        try{
            ResultSet resultado = objetoConexion.consultarRegistros("SELECT * FROM Lider ORDER BY Ciudad_Residencia");
            while(resultado.next()){
                resultado.getString("ID_Lider");
                resultado.getString("Nombre");
                resultado.getString("Primer_Apellido");
                resultado.getString("Ciudad_Residencia");
                Object[] oUser = {resultado.getString("ID_Lider"),resultado.getString("Nombre"),resultado.getString("Primer_Apellido"),resultado.getString("Ciudad_Residencia")};
                modelo.addRow(oUser);
            }
        }
        catch(Exception e){
            System.out.println(e);
        }
        
    }
